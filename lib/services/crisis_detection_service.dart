class CrisisDetectionService {
  static const _crisisKeywords = [
    'самоубийство',
    'самоубийства',
    'убить себя',
    'повеситься',
    'яд',
    'ножом',
    'вены',
    'порезать',
    'порезаться',
    'передоз',
    'таблетки',
    'лекарства',
    'прыгну',
    'спрыгну',
    'машину',
    'поезд',
    'вред себе',
    'причинить себе',
    'больше не хочу жить',
    'не вижу смысла',
    'лучше бы умер',
    'хочу умереть',
    'все бесполезно',
    'никому не нужен',
  ];

  static bool detectCrisis(String message) {
    final lowerMessage = message.toLowerCase();

    for (var keyword in _crisisKeywords) {
      if (lowerMessage.contains(keyword)) {
        return true;
      }
    }

    return false;
  }

  static String getCrisisResponse() {
    return 'С тобой всё не в порядке — и это важно.\n\n'
        'В твоём сообщении я заметил слова, которые указывают на сильную боль и риск причинения вреда себе.\n\n'
        'Я не могу безопасно поддерживать тебя в такой ситуации. '
        'Очень важно, чтобы рядом был живой человек, который сможет помочь.\n\n'
        'Пожалуйста, обратись к профессионалу или позвони на горячую линию прямо сейчас.';
  }
}
